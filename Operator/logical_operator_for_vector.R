#logical Operator for vector
c(TRUE,TRUE) &   c(TRUE,FALSE)  #Vectorized AND
c(TRUE,TRUE) |   c(TRUE,FALSE)  #Vectorized OR

!c(TRUE,TRUE)                   #Vectorized NOT
c(1,2) %in% c(1,3,4,5)        #Vectorized IN
1 %in% c(1,2,3,4,5)             #IN With Scalar value



TRUE & TRUE
TRUE & FALSE
FALSE & TRUE
FALSE & FALSE



TRUE | TRUE
TRUE | FALSE
FALSE | TRUE
FALSE | FALSE
